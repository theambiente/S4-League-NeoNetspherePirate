  internal class AddCapsule
  {
    public static string ConvertCapsuleReward(string reward)
    {
      if (reward == null)
        reward = "";

      if (reward.Length < 1)
        return reward;

      if (char.IsNumber(reward[0]))
        reward = "_" + reward;

      reward = reward.Replace(".", "_");

      return (string)reward.Clone();
    }

    public AddCapsule(ItemNumber capsuleItemId)
    {
      CapsuleItemId = capsuleItemId;
    }

    public ItemNumber CapsuleItemId { get; set; }

    public bool Ready { get; set; }

    public Dictionary<int, AddCapsuleReward> Prizes = new Dictionary<int, AddCapsuleReward>();
  }

  internal class AddCapsuleReward
  {
    public int SlotId { get; set; }

    public AddCapsuleReward(int slotId)
    {
      SlotId = slotId;
      Items = new Dictionary<ShopItem, int>();
      Rewards = new List<CapsuleReward>();
    }

    public Dictionary<ShopItem, int> Items { get; set; }

    public List<CapsuleReward> Rewards { get; set; }
  }

